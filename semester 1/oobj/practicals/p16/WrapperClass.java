
public class WrapperClass {
     public static void main(String [] args){
        // byte into Byte object
        System.out.println("------------------");
        byte b = 5;
        Byte byteobj = b;
        System.out.println("Byte object : "+byteobj);
        System.out.println("------------------");

        // short into Short object 
        System.out.println("------------------");
        short s = 15;
        Short shortobj = s;
        System.out.println("Short object : "+shortobj);
        System.out.println("------------------");

        // int into Integer object 
        System.out.println("------------------");
        int i = 250;
        Integer intobj = i;
        System.out.println("Integer object : "+intobj);
        System.out.println("------------------");

        // long into Long object
        System.out.println("------------------"); 
        long l = 1000;
        Long longobj = l;
        System.out.println("Long Object : "+longobj);
        System.out.println("------------------");
        
        // float into Float object
        System.out.println("------------------"); 
        float f = 125.65f;
        Float floatobj = f;
        System.out.println("Float object : "+floatobj);
        System.out.println("------------------");

        // double into Double object 
        System.out.println("------------------");
        double d = 1524.21;
        Double doubleobj = d;
        System.out.println("Double object : "+doubleobj);
        System.out.println("------------------");

        // char into Character object
        System.out.println("------------------");  
        char c = 'A';
        Character charobj = c;
        System.out.println("Character Object : "+charobj);
        System.out.println("------------------");

        // boolean into Boolean object 
        System.out.println("------------------");
        boolean bool= true;
        Boolean boolobj = bool;
        System.out.println("Boolean object : "+boolobj); 
        System.out.println("------------------");
     }
}
